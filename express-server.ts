/////////////////////////////////////////////////////////////////////
// Modules
/////////////////////////////////////////////////////////////////////

const express: Express = require('express');
const morgan = require('morgan');

/////////////////////////////////////////////////////////////////////
// Set-Up
/////////////////////////////////////////////////////////////////////

const app = express();
const PORT = 8080;

/////////////////////////////////////////////////////////////////////
// Middleware
/////////////////////////////////////////////////////////////////////

app.use(morgan('dev'));

/////////////////////////////////////////////////////////////////////
// Listener
/////////////////////////////////////////////////////////////////////

app.listen(PORT, () => console.log(
    `Express Application is now listening on: http://localhost:${PORT}/`
));

/////////////////////////////////////////////////////////////////////
// Routes
/////////////////////////////////////////////////////////////////////

app.get('/', (req: Request, res: Response) => {
    res.status(200);
    res.send(`
        <!DOCTYPE html>
        <html>
            <head><title>Home Page</title></head>
            <body><h1>Home Page</h1></body>
        </html>
    `.replace(/        /g, ''));
});
