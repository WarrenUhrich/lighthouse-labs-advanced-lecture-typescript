/////////////////////////////////////////////////////////////////////
// Modules
/////////////////////////////////////////////////////////////////////
var express = require('express');
var morgan = require('morgan');
/////////////////////////////////////////////////////////////////////
// Set-Up
/////////////////////////////////////////////////////////////////////
var app = express();
var PORT = 8080;
/////////////////////////////////////////////////////////////////////
// Middleware
/////////////////////////////////////////////////////////////////////
app.use(morgan('dev'));
/////////////////////////////////////////////////////////////////////
// Listener
/////////////////////////////////////////////////////////////////////
app.listen(PORT, function () { return console.log("Express Application is now listening on: http://localhost:".concat(PORT, "/")); });
/////////////////////////////////////////////////////////////////////
// Routes
/////////////////////////////////////////////////////////////////////
app.get('/', function (req, res) {
    res.status(200);
    res.send("\n        <!DOCTYPE html>\n        <html>\n            <head><title>Home Page</title></head>\n            <body><h1>Home Page</h1></body>\n        </html>\n    ".replace(/        /g, ''));
});
